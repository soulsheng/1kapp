public class ch2ex14{
	public static void main(String[]args){
		int x=10;
		int y=-13;
		//find max of x,y
		int l=Math.max(x,y);
		System.out.println(l);
		//find the absolute of y
		l=Math.abs(y);
		System.out.println(l);
		//compute 3 to the power of 4
		double t=Math.pow(3,4);
		System.out.println(t);
		//test round
		l=(int)(Math.round(3.55));
		System.out.println(l);
	}
}
